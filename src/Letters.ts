export function getLetter(letter:string) {
	switch (letter) {
		case " ":
			return SPACE;
		case "_":
			return HEART;
		case "A":
			return A;
		case "B":
			return B;
		case "C":
			return C;
		case "D":
			return D;
		case "E":
			return E;
		case "F":
			return F;
		case "G":
			return G;
		case "H":
			return H;
		case "I":
			return I;
		case "J":
			return J;
		case "K":
			return K;
		case "L":
			return L;
		case "M":
			return M;
		case "N":
			return N;
		case "O":
			return O;
		case "P":
			return P;
		case "Q":
			return Q;
		case "R":
			return R;
		case "S":
			return S;
		case "T":
			return T;
		case "U":
			return U;
		case "V":
			return V;
		case "W":
			return W;
		case "X":
			return X;
		case "Y":
			return Y;
		case "Z":
			return Z;
		case "0":
			return ZERO;
		case "1":
			return ONE;
		case "2":
			return TWO;
		case "3":
			return THREE;
		case "4":
			return FOUR;
		case "5":
			return FIVE;
		case "6":
			return SIX;
		case "7":
			return SEVEN;
		case "8":
			return EIGHT;
		case "9":
			return NINE;
		//specials
		case "!":
			return SPECIAL_ONE;
		case "@":
			return SPECIAL_TWO;
		case "#":
			return SPECIAL_THREE;
		case "$":
			return SPECIAL_FOUR;
		case "%":
			return SPECIAL_FIVE;
		case "^":
			return SPECIAL_SIX;
		case "&":
			return SPECIAL_SEVEN;
		case "*":
			return SPECIAL_EIGHT;
	}
}

